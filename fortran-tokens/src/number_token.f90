module number_token
  implicit none

  !> Number Tokens
  character(len=1), parameter :: ZERO = '0'
  character(len=1), parameter :: ONE = '1'
  character(len=1), parameter :: TWO = '2'
  character(len=1), parameter :: THREE = '3'
  character(len=1), parameter :: FOUR = '4'
  character(len=1), parameter :: FIVE = '5'
  character(len=1), parameter :: SIX = '6'
  character(len=1), parameter :: SEVEN = '7'
  character(len=1), parameter :: EIGHT = '8'
  character(len=1), parameter :: NINE = '9'

end module number_token

