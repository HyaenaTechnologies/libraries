module delimiter_token
  implicit none

  !> Delimiter Tokens
  character(len=1), parameter :: LEFT_BRACKET = '{'
  character(len=1), parameter :: LEFT_PARENTHESIS = '('
  character(len=1), parameter :: LEFT_SQUARE_BRACKET = '['
  character(len=1), parameter :: RIGHT_BRACKET = '}'
  character(len=1), parameter :: RIGHT_PARENTHESIS = ')'
  character(len=1), parameter :: RIGHT_SQUARE_BRACKET = ']'

end module delimiter_token

