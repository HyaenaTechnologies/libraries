module delimiter_token
    implicit none

    !> Delimiter Tokens
    character(len = 1)::LEFT_BRACKET = "{"
    character(len = 1)::LEFT_PARENTHESIS = "("
    character(len = 1)::LEFT_SQUARE_BRACKET = "["
    character(len = 1)::RIGHT_BRACKET = "}"
    character(len = 1)::RIGHT_PARENTHESIS = ")"
    character(len = 1)::RIGHT_SQUARE_BRACKET = "]"

end module delimiter_token

