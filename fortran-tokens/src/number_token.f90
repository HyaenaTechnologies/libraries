  character(len=1) :: FOUR = "4"
  character(len=1) :: FIVE = "5"
  character(len=1) :: SIX = "6"
  character(len=1) :: SEVEN = "7"
  character(len=1) :: EIGHT = "8"
  character(len=1) :: NINE = "9"

end module number_token

